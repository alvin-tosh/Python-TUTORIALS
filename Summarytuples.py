    # Tuples are immutable lists.
    # A tuple is like a list except that it uses parentheses () instead of square brackets [].
    rgb = ('red', 'green', 'blue')
    # Use tuples when you want to define a list that cannot change.
    # Even though you can’t change a tuple, you can assign a new tuple to a variable that references a tuple.
    # To define a tuple with one element, you need to include a trailing comma after the first element. 
    numbers = (3,) # Tuple with single element
    # If you exclude the trailing comma, the type of the numbers will be int , which stands for integer. And its value is 3. Python won’t create a tuple that includes the number 3
    numbers = (3) # Python won’t create a tuple that includes the number 3
    
    
